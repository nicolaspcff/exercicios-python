dia = input("Digite o numero do dia da semana: ")
if dia == '1':
    print("O dia é domingo")
elif dia == '2':
    print("O dia é segunda ")
elif dia == '3':
    print("O dia é terca")
elif dia == '4':
    print("O dia é quarta")
elif dia == '5':
    print("O dia é quinta")
elif dia == '6':
    print("O dia é sexta")
elif dia == '7':
    print("O dia é sabado")
else:
    print("numero invalido")